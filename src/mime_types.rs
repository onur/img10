pub fn from_file_name(name: &str) -> &str {
    Path::new(name)
        .extension()
        .map(|e| e.to_string_lossy())
        .map(|e| e.to_lowercase())
        .and_then(|e| MIME_TYPES.get(&*e))
        .unwrap_or(&"application/octet-stream")
}
